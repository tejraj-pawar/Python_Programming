'''
(Turtle: draw a star) Write a program that draws a star, as shown in Figure 1.19c.
(Hint: The inner angle of each point in the star is 36 degrees.)
'''

import turtle

turtle.showturtle()

turtle.forward(200)
turtle.left(324)
turtle.backward(200)
turtle.left(324)
turtle.forward(200)
turtle.left(324)
turtle.backward(200)
turtle.left(324)
turtle.forward(200)
turtle.left(324)

turtle.done()